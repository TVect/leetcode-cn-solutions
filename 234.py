# Definition for singly-linked list.
class ListNode:
    def __init__(self, x):
        self.val = x
        self.next = None


class Solution:

    def isPalindrome(self, head: ListNode) -> bool:
        vals = []
        current_node = head
        while current_node is not None:
            vals.append(current_node.val)
            current_node = current_node.next
        return vals == vals[::-1]

    # O(n) 时间复杂度和 O(1) 空间复杂度 的做法，需要修改链表
    # e.g. 用快慢指针找到链表中点后，翻转链表前半部或者后半部，再逐个进行比较
